class Algorithms:
    @staticmethod
    def sample_algorithm(input_data):
        """
        This is a sample algorithm that doubles each element in the input list.
        Args:
            input_data (list): A list of input data for the algorithm.
        
        Returns:
            output_data: The result of the algorithm processing the input data.
        """
        output_data = []

        # Sample algorithm logic (doubling each element in the input list)
        for item in input_data:
            output_data.append(item * 2)

        return output_data
